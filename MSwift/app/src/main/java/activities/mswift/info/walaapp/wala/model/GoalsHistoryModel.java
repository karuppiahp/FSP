public class GoalsHistoryModel {
    private String goalsName;
    private  String goalsPoints;
    private String goalsDate;
    private String goalsTarget;
    private String goalsType;

    public String getGoalsName() {
        return goalsName;
    }

    public void setGoalsName(String goalsName) {
        this.goalsName = goalsName;
    }

    public String getGoalsPoints() {
        return goalsPoints;
    }

    public void setGoalsPoints(String goalsPoints) {
        this.goalsPoints = goalsPoints;
    }

    public String getGoalsDate() {
        return goalsDate;
    }

    public void setGoalsDate(String goalsDate) {
        this.goalsDate = goalsDate;
    }

    public String getGoalsTarget() {
        return goalsTarget;
    }

    public void setGoalsTarget(String goalsTarget) {
        this.goalsTarget = goalsTarget;
    }

    public String getGoalsType() {
        return goalsType;
    }

    public void setGoalsType(String goalsType) {
        this.goalsType = goalsType;
    }
}
